extern uint16_t TimerPeriod;  // ARR自动重装值，决定PWM频率


// 全局变量，用来存放三相新比较值
uint16_t ccr1_val = 2000;
uint16_t ccr2_val = 1000;
uint16_t ccr3_val = 500;

void TIM1_UP_IRQHandler(void)
{
    // 判断是否是更新中断标志
    if(TIM_GetITStatus(TIM1, TIM_IT_Update) != RESET)
    {
        // ========== 在这里修改PWM占空比 ==========
        TIM_SetCompare1(TIM1, ccr1_val);
        TIM_SetCompare2(TIM1, ccr2_val);
        TIM_SetCompare3(TIM1, ccr3_val);

        // 示例：简单动态变化占空比（测试用）
        ccr1_val += 50;
        if(ccr1_val > TimerPeriod)
            ccr1_val = 0;

        // 清除中断标志位（必须！否则一直进中断）
        TIM_ClearITPendingBit(TIM1, TIM_IT_Update);
    }
}

